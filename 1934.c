#include <stdio.h>
// 유클리드 호제법
int main(void)
{
	int t;
	int a, b, c;
	scanf("%d", &t);

	for (int i = 0; i < t; i++)
	{
		scanf("%d %d", &a, &b);
		while (b)
		{
			c = a % b;
			a = b;
			b = c;
		}
		printf("%d\n", a);
	}

	return 0;
}
