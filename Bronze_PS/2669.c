#include <stdio.h>

int main(void)
{
	int map[101][101] = {0,};
	int x1, x2;
	int y1, y2;
	int res = 0;

	for (int i = 0; i < 4; i++)
	{
		scanf("%d %d %d %d", &x1, &y1, &x2, &y2);
		for (int j = x1; j < x2; j++)
			for (int k = y1; k < y2; k++)
				map[j][k] = 1;
	}
	for (int i = 0; i < 101; i++)
	{
		for(int j = 0; j < 101; j++)
		{
			if (map[i][j])
				res++;
		}
	}
	printf("%d", res);

	return 0;
}
