#include <stdio.h>

int main(void)
{
	char str[105];
	char ret[105];
	int i = 0;
	int j = 0;

	scanf("%s", str);
	while(str[i])
	{
		if ('A' <= str[i] && str[i] <= 'Z')
		{
			ret[j] = str[i];
			i++;
			j++;
		}
		else
		{
			i++;
		}
	}

	printf("%s", ret);

	return 0;
}
