#include <stdio.h>

int main(void)
{
	unsigned long long a, b, c;

	scanf("%llu %llu %llu", &a, &b, &c);

	if (b >= c)
	{
		printf("-1");
		return 0;
	}

	printf("%lld", (a / (c - b)) + 1);
	
	return 0;
}
