#include <stdio.h>

int main(void)
{
	int a, b;

	scanf("%d %d", &a, &b);

	int i;
	
	i = a < b ? a : b;
	while (1)
	{
		if (a % i == 0 && b % i == 0)
		{
			printf("%d\n", i);
			break;
		}
		i--;
	}
	i = a > b ? a : b;
	while (1)
	{
		if (i % a == 0 && i % b == 0)
		{
			printf("%d", i);
			break;
		}
		i++;
	}
	
	return 0;
}
