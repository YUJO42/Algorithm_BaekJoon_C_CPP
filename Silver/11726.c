#include <stdio.h>

int arr[1010] = {0, };

int main(void)
{
	int n;

	arr[0] = 0;
	arr[1] = 1;
	arr[2] = 2;
	scanf("%d", &n);
	for (int i = 3; i <= n; i++)
		arr[i] = (arr[i - 1] + arr[i - 2]) % 10007;
	printf("%d", arr[n]);

	return 0;
}
