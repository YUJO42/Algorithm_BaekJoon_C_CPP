#include <stdio.h>

int main(void)
{
	int n;
	int m;
	int count = 0;

	scanf("%d", &n);

	for (int i = 0; i < 5; i++)
	{
		scanf("%d", &m);
		if (m == n)
			count++;
	}
	printf("%d", count);

	return 0;
}
