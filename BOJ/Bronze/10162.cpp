#include <iostream>

using namespace std;

int main(void)
{
	int t;

	cin >> t;

	int a = 0;
	int b = 0;
	int c = 0;
	// a = 300
	// b = 60
	// c = 10
	
	if ((t % 10) != 0)
	{
		cout << "-1";
		return 0;
	}

	while(t)
	{
		if (t >= 300)
		{
			t -= 300;
			a++;
		}
		else if (t >= 60)
		{
			t -= 60;
			b++;
		}
		else
		{
			t -= 10;
			c++;
		}
	}

	cout << a << ' ' << b << ' ' << c;

	return 0;
}
