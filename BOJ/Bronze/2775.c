#include <stdio.h>

int main(void)
{
	int t;
	int k, n;
	int a[15][15] = {0, };

	for (int i = 1; i < 15; i++)
		a[0][i] = i;

	for (int i = 0; i < 15; i++)
		a[i][1] = 1;
	
	scanf("%d", &t);

	for (int i = 1; i < 15; i++)
	{
		for (int j = 1; j < 15; j++)
			a[i][j] = a[i - 1][j] + a[i][j - 1]; 
	}

	for (int i = 0; i < t; i++)
	{
		scanf("%d", &k); // k = 층
		scanf("%d", &n); // n = 호

		printf("%d\n", a[k][n]);
	}

	return 0;
}
