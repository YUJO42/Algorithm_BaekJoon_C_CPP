#include <stdio.h>

int main(void)
{
	long long n, m;
	long long ret;

	scanf("%lld %lld", &n, &m);
	ret = n - m;
	if (ret < 0)
		ret *= -1;
	printf("%lld", ret);

	return 0;
}
