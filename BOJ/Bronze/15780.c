#include <stdio.h>

int main(void)
{
	int n, k;
	int m;
	int sum = 0;

	scanf("%d %d", &n, &k);

	for (int i = 0; i < k; i++)
	{
		scanf("%d", &m);
		if (m % 2 == 1)
			sum += (m / 2) + 1;
		else
			sum += (m / 2);
	}

	if (n <= sum)
		printf("YES");
	else
		printf("NO");

	return 0;
}
