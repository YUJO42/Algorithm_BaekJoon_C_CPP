#include <stdio.h>

int main(void)
{
	int t, a, b;

	scanf("%d", &t);
	for (int i = 0; i < t; i++)
	{
		scanf("%d %d", &a, &b);
		int one = 1;
		for (int j = 0; j < b;  j++)
		{
			one *= a;
			one %= 10;
		}
		if (one == 0)
			printf("10\n");
		else
			printf("%d\n", one); 
	}
	return 0;
}
