#include <stdio.h>

int main(void)
{
	int n;
	scanf("%d", &n);

	int x;
	int count = 0;
	int arr[101] = {0, };
	for (int i = 0; i < n; i++)
	{
		scanf("%d", &x);
		if (arr[x] == 0)
			arr[x] = 1;
		else
			count++;
	}

	printf("%d", count);
	
	return 0;
}
