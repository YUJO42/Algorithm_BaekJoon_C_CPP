#include <stdio.h>

int main(void)
{
	int n;

	scanf("%d", &n);

	int x = 0;
	int d;

	while (x * (x + 1) / 2 < n)
		x++;

	x--;
	d = n - x * (x + 1) / 2;
	if (x % 2)
		printf("%d/%d\n", d, x + 2 - d);
	else
		printf("%d/%d\n", x + 2 - d, d);

	return 0;
}
