#include <stdio.h>

int main(void)
{
	int a, b, c, d;
	int max = -1;
	int winner = 0;

	for (int i = 0; i < 5; i++)
	{
		scanf("%d %d %d %d", &a, &b, &c, &d);
		if (max < a + b + c + d)
		{
			max = a + b + c + d;
			winner = i + 1;
		}
	}

	printf("%d %d", winner, max);

	return 0;
}
