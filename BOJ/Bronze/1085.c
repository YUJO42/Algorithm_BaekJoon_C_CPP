#include <stdio.h>

int main(void)
{
	int x, y, w, h;
	int X, Y;

	scanf("%d %d %d %d", &x, &y, &w, &h);
	
	if (x < w - x)
		X = x;
	else 
		X = w - x;
	if (y < h - y)
		Y = y;
	else
		Y = h - y;
	
	printf("%d", X < Y ? X : Y);

	return 0;
}
