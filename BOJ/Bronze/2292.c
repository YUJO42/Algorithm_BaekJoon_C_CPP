#include <stdio.h>

int main(void)
{
	int n;
	int ret = 0;

	scanf("%d", &n);

	if (n == 1)
	{
		printf("0");
		return 0;
	}
	
	for (int i = 2; i <= n; ret++)
		i += 6 * ret;

	printf("%d", ret);


	return 0;
}
