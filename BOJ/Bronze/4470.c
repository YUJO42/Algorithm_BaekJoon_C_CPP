#include <stdio.h>

int main(void)
{
	int n;
	char str[55];
	
	scanf("%d", &n);
	getchar();

	for (int i = 1; i <= n; i++)
	{
		scanf("%[^\n]s", str);
		getchar();

		printf("%d. %s\n", i, str);
	}

	return 0;
}
