#include <stdio.h>

int fibo(int n)
{
	int arr[50] = {0, };
	arr [0] = 0;
	arr [1] = 1;

	for (int i = 0; i < n; i++)
		arr[i + 2] = arr[i + 1] + arr[i];

	return arr[n];
}

int main(void)
{
	int n;

	scanf("%d", &n);
	printf("%d", fibo(n));

	return 0;
}
