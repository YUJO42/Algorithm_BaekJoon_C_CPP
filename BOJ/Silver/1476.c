#include <stdio.h>

int year = 0;
int x, y, z;

int main(void)
{
	int e, m, s;

	scanf("%d %d %d", &e, &m, &s);

	while (1)
	{
		x =	year % 15;
		y = year % 28;
		z = year % 19;
		if (x == e - 1 && y == m - 1 && z == s - 1 && year < 7980)
		{
			printf("%d", year + 1);
			break;
		}
		year++;
	}

	return 0;
}
