#include <stdio.h>

int main(void)
{
	long long dp[102];
	dp[1] = 1;
	dp[2] = 1;
	dp[3] = 1;
	dp[4] = 2;
	dp[5] = 2;
	dp[6] = 3;

	for (int i = 6; i < 101; i++)
		dp[i] = dp[i - 1] + dp[i - 5];

	int N;
	scanf("%d", &N);

	while(N--)
	{
		int n;
		scanf("%d", &n);
		printf("%lld\n", dp[n]);
	}

	return 0;
}
