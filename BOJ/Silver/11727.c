#include <stdio.h>

int arr[1001] = {0, };

int tile(int n)
{
	arr[0] = 0;
	arr[1] = 1;
	arr[2] = 3;

	if (arr[n])
		return arr[n];
	arr[n] = (tile(n - 1) + 2 * tile(n - 2)) % 10007;
	return arr[n];
}

int main(void)
{
	int n;
	scanf("%d", &n);
	printf("%d", tile(n));

	return 0;
}
