#include <stdio.h>

int main(void)
{
	int n;
	scanf("%d", &n);
	
	int arr[n];
	int y = 0; // 30
	int m = 0; // 60

	for (int i = 0; i < n; i++)
		scanf("%d", &arr[i]);

	for (int i = 0; i < n; i++)
	{
		y += (arr[i] / 30) + 1;
		m += (arr[i] / 60) + 1;
	}

	y *= 10;
	m *= 15;

	if (y == m)
		printf("Y M %d", y);
	else
		printf("%c %d", y < m ? 'Y' : 'M', y < m ? y : m);

	return 0;
}
