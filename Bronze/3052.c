#include <stdio.h>

int main(void)
{
	int arr[11];
	int check[42] = {0, };
	int count = 0;

	for (int i = 0; i < 10; i++)
		scanf("%d", &arr[i]);

	for (int i = 0; i < 10; i++)
		check[arr[i] % 42] = 1;

	for (int i = 0; i < 42; i++)
		if (check[i] == 1)
			count++;
	
	printf("%d", count);

	return 0;
}
