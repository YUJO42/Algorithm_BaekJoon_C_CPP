#include <stdio.h>

int main(void)
{
	int n;

	scanf("%d", &n);

	if (n == 1)
		printf("*");
	else
	{
		for (int i = 0; i < n; i++)
		{
			if (i % 2 == 0)
			{
				for (int j = 0; j < n * 2; j++)
				{
					if (j % 2 == 0)
						printf("*");
					else
						printf(" ");
				}
				printf("\n");
			}
			else// (n % 2 == 1)
			{
				for (int k = 0; k < n * 2; k++)
				{
					if (k % 2 == 1)
						printf("*");
					else
						printf(" ");
				}
				printf("\n");
			}
		}
	}
}
