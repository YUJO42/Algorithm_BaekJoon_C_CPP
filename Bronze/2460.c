#include <stdio.h>

int main(void)
{
	int a, b;
	int max = -1;
	int sum = 0;

	for (int i = 0; i < 10; i++)
	{
		scanf("%d %d", &a, &b);
		sum += b - a;
		if (max < sum)
			max = sum;
	}

	printf("%d", max);

	return 0;
}
