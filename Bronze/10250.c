#include <stdio.h>

int main(void)
{
	int t;
	int h, w, n;

	scanf("%d", &t);

	for (int i = 0; i < t; i++)
	{
		scanf("%d %d %d", &h, &w, &n);
		n--;
		printf("%d", n % h + 1);
		if (n / h + 1 < 10)
			printf("0");
		printf("%d\n", n / h + 1);
	}

	return 0;
}
