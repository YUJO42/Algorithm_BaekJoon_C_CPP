#include <stdio.h>

int main(void)
{
	int n;

	scanf("%d", &n);
	
	char str[n][55];

	for (int i = 0; i < n; i++)
		scanf("%s", str[i]);

	int k;
	for (int i = 0; i < n; i++)
	{
		for (int k = 0; str[i][k]; k++)
		{
			if (str[0][k] != '?' && str[0][k] != str[i][k])
			{
				str[0][k] = '?';
			}
		}
	}
	printf("%s", str[0]);
	return 0;
}
